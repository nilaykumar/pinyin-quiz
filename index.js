const scoreDiv = document.getElementById('score');

var counter = 0;
var correctCounter = 0;
var randomIndex = null;
var lastChoice = null;
var choice = null;
var currentSyllable = null;
function chooseSyllable() {
    lastChoice = choice;
    randomIndex = Math.floor(Math.random() * syllableFiles.length);
    choice = syllableFiles[randomIndex];
    currentSyllable = choice.slice(0, -4);
}
chooseSyllable();

function play() {
    var audio = new Audio(`data/pinyin-male/${choice}`);
    audio.play();
}

function inputHandler(event) {
    var s = inputText.value.trim().toLowerCase();
    if(event.key === "Enter" && s) {
        counter ++;
        event.preventDefault();

        var correct = s === currentSyllable;
        if(correct)
            correctCounter ++;
        var correctStr = correct ? '✓' : "✕";

        var content = document.createElement('p');
        content.textContent = `${correctStr} ${s} (${currentSyllable})`;
        outputDiv.prepend(content);

        inputText.value = "";
        var scoreContent = document.createElement('p');
        scoreDiv.innerText = `${correctCounter} / ${counter}`;

        chooseSyllable();
        play();
    }
}

playButton.addEventListener('click', play);
inputText.addEventListener('keypress', inputHandler);
